/** Public products — excludes all medicines */
export const publicProducts = products.filter((p) => !p.isMedicine);

/** Category nav items for public display — excludes Medicine category */
export const publicCategoryNavItems = categoryNavItems.filter(
  (c) => c.slug !== "medicine",
);

export const getProductsByCategory = (category: string) =>
  products.filter((p) => p.category === category);

export const getProductById = (id: string) =>
  products.find((p) => p._id === id);

export const getRelatedProducts = (product: Product) =>
  products
    .filter(
      (p) =>
        p.category === product.category &&
        p._id !== product._id &&
        !p.isMedicine,
    )
    .slice(0, 6);
