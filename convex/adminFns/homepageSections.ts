/**
 * List all eligible (non-medicine, non-prescription, non-deleted) products
 * for the admin to pick from when configuring homepage sections.
 * Enriches each product with its brand name for display in the admin UI.
 */
export const listEligibleProducts = query({
  args: {},
  returns: v.array(v.any()),
  handler: async (ctx) => {
    const products = await ctx.db
      .query("products")
      .withIndex("by_isDeleted_and_name", (q) => q.eq("isDeleted", undefined))
      .collect();

    const eligible = products.filter(
      (p) => !p.isMedicine && !p.isPrescriptionControlled,
    );

    // Enrich with brand names
    const enriched = await Promise.all(
      eligible.map(async (p) => {
        let brandName: string | null = null;
        if (p.brandId) {
          const brand = await ctx.db.get(p.brandId);
          brandName = brand?.name ?? null;
        }
        return { ...p, brandName };
      }),
    );

    return enriched;
  },
});

