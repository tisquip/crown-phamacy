/**
 * Reset "Shop by Category" to auto-select:
 * - Unique categories from the products with the highest purchaseCount.
 * - Excludes categories that only contain medicine/prescription products.
 * - Takes up to 8 categories.
 */
export const resetShopByCategory = mutation({
  args: {},
  returns: v.null(),
  handler: async (ctx) => {
    const userId = await getAuthUserId(ctx);
    if (!userId) throw new Error("Not authenticated");

    // Get all non-deleted, non-medicine products sorted by purchaseCount desc
    const products = await ctx.db
      .query("products")
      .withIndex("by_isDeleted_and_purchaseCount", (q) =>
        q.eq("isDeleted", undefined),
      )
      .order("desc")
      .collect();

    const eligibleProducts = products.filter(
      (p) => !p.isMedicine && !p.isPrescriptionControlled,
    );

    // Collect unique category IDs in order of first appearance
    // (products are already sorted by purchaseCount desc)
    const seenCategoryIds = new Set<string>();
    const orderedCategoryIds: Array<Id<"productCategory">> = [];

    for (const product of eligibleProducts) {
      if (!product.productCategoryIds) continue;
      for (const catId of product.productCategoryIds) {
        if (!seenCategoryIds.has(catId)) {
          seenCategoryIds.add(catId);
          orderedCategoryIds.push(catId);
        }
      }
      if (orderedCategoryIds.length >= 8) break;
    }

    // Verify categories exist and are not deleted
    const validCategoryIds: Array<Id<"productCategory">> = [];
    for (const catId of orderedCategoryIds.slice(0, 8)) {
      const cat = await ctx.db.get(catId);
      if (cat && !cat.isDeleted) {
        validCategoryIds.push(catId);
      }
    }

    const existing = await ctx.db
      .query("homepageSection")
      .withIndex("by_sectionType", (q) => q.eq("sectionType", "shopByCategory"))
      .unique();

    if (existing) {
      await ctx.db.patch(existing._id, {
        categoryIds: validCategoryIds,
        isAutoGenerated: true,
        lastModifiedBy: userId,
        lastModifiedAt: Date.now(),
      });
    } else {
      await ctx.db.insert("homepageSection", {
        sectionType: "shopByCategory",
        categoryIds: validCategoryIds,
        isAutoGenerated: true,
        lastModifiedBy: userId,
        lastModifiedAt: Date.now(),
      });
    }
    return null;
  },
});

