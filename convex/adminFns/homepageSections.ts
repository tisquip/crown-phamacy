/**
 * Save (create or update) a homepage section with manually chosen items.
 * Validates that no medicine or prescription-controlled products are included.
 */
export const saveSection = mutation({
  args: {
    sectionType: sectionTypeValidator,
    productIds: v.optional(v.array(v.id("products"))),
    categoryIds: v.optional(v.array(v.id("productCategory"))),
  },
  returns: v.null(),
  handler: async (ctx, args) => {
    const userId = await getAuthUserId(ctx);
    if (!userId) throw new Error("Not authenticated");

    // Validate products are not medicine / prescription-controlled
    if (args.productIds) {
      for (const productId of args.productIds) {
        const product = await ctx.db.get(productId);
        if (!product) throw new Error(`Product ${productId} not found`);
        if (product.isMedicine || product.isPrescriptionControlled) {
          throw new Error(
            `Product "${product.name}" is medicine or prescription-controlled and cannot be displayed in homepage sections`,
          );
        }
        if (product.isDeleted) {
          throw new Error(
            `Product "${product.name}" has been deleted and cannot be displayed`,
          );
        }
      }
    }

    const existing = await ctx.db
      .query("homepageSection")
      .withIndex("by_sectionType", (q) => q.eq("sectionType", args.sectionType))
      .unique();

    if (existing) {
      await ctx.db.patch(existing._id, {
        productIds: args.productIds,
        categoryIds: args.categoryIds,
        isAutoGenerated: false,
        lastModifiedBy: userId,
        lastModifiedAt: Date.now(),
      });
    } else {
      await ctx.db.insert("homepageSection", {
        sectionType: args.sectionType,
        productIds: args.productIds,
        categoryIds: args.categoryIds,
        isAutoGenerated: false,
        lastModifiedBy: userId,
        lastModifiedAt: Date.now(),
      });
    }
    return null;
  },
});

/**
 * Reset "Top Sellers" to auto-select:
 * - Non-medicine, non-prescription products sorted by purchaseCount DESC.
 * - Takes the top 12 products.
 */
export const resetTopSellers = mutation({
  args: {},
  returns: v.null(),
  handler: async (ctx) => {
    const userId = await getAuthUserId(ctx);
    if (!userId) throw new Error("Not authenticated");

    // Get all non-deleted products sorted by purchaseCount descending
    const products = await ctx.db
      .query("products")
      .withIndex("by_isDeleted_and_purchaseCount", (q) =>
        q.eq("isDeleted", undefined),
      )
      .order("desc")
      .collect();

    const eligible = products
      .filter((p) => !p.isMedicine && !p.isPrescriptionControlled)
      .slice(0, 12);

    const productIds: Array<Id<"products">> = eligible.map((p) => p._id);

    const existing = await ctx.db
      .query("homepageSection")
      .withIndex("by_sectionType", (q) => q.eq("sectionType", "topSellers"))
      .unique();

    if (existing) {
      await ctx.db.patch(existing._id, {
        productIds,
        isAutoGenerated: true,
        lastModifiedBy: userId,
        lastModifiedAt: Date.now(),
      });
    } else {
      await ctx.db.insert("homepageSection", {
        sectionType: "topSellers",
        productIds,
        isAutoGenerated: true,
        lastModifiedBy: userId,
        lastModifiedAt: Date.now(),
      });
    }
    return null;
  },
});

/**
 * Reset "Items on Promotion" to auto-select:
 * - Non-medicine, non-prescription products that have a promotionPriceInUSDCents.
 * - Sorted by purchaseCount DESC.
 * - Takes the top 12 products.
 */
