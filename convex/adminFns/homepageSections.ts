/**
 * Save the promo banner configuration (image, texts, link).
 */
export const savePromoBanner = mutation({
  args: {
    storageId: v.optional(v.id("_storage")),
    cdnImageUrl: v.optional(v.string()),
    cdnImageKey: v.optional(v.string()),
    badgeText: v.optional(v.string()),
    headlineText: v.optional(v.string()),
    buttonText: v.optional(v.string()),
    buttonLink: v.optional(v.string()),
  },
  returns: v.null(),
  handler: async (ctx, args) => {
    const userId = await getAuthUserId(ctx);
    if (!userId) throw new Error("Not authenticated");

    const existing = await ctx.db
      .query("homepageSection")
      .withIndex("by_sectionType", (q) => q.eq("sectionType", "promoBanner"))
      .unique();

    if (existing) {
      // Delete old CDN image if it changed
      if (existing.cdnImageKey && existing.cdnImageKey !== args.cdnImageKey) {
        await ctx.scheduler.runAfter(0, api.cdn.deleteFile, {
          key: existing.cdnImageKey,
        });
      }

      await ctx.db.patch(existing._id, {
        storageId: args.storageId,
        cdnImageUrl: args.cdnImageUrl,
        cdnImageKey: args.cdnImageKey,
        badgeText: args.badgeText,
        headlineText: args.headlineText,
        buttonText: args.buttonText,
        buttonLink: args.buttonLink,
        isAutoGenerated: false,
        lastModifiedBy: userId,
        lastModifiedAt: Date.now(),
      });
    } else {
      await ctx.db.insert("homepageSection", {
        sectionType: "promoBanner",
        isAutoGenerated: false,
        storageId: args.storageId,
        cdnImageUrl: args.cdnImageUrl,
        cdnImageKey: args.cdnImageKey,
        badgeText: args.badgeText,
        headlineText: args.headlineText,
        buttonText: args.buttonText,
        buttonLink: args.buttonLink,
        lastModifiedBy: userId,
        lastModifiedAt: Date.now(),
      });
    }
    return null;
  },
});

/**
 * Clear (delete) the promo banner configuration entirely.
 */
export const clearPromoBanner = mutation({
  args: {},
  returns: v.null(),
  handler: async (ctx) => {
    const userId = await getAuthUserId(ctx);
    if (!userId) throw new Error("Not authenticated");

    const existing = await ctx.db
      .query("homepageSection")
      .withIndex("by_sectionType", (q) => q.eq("sectionType", "promoBanner"))
      .unique();

    if (existing) {
      if (existing.cdnImageKey) {
        await ctx.scheduler.runAfter(0, api.cdn.deleteFile, {
          key: existing.cdnImageKey,
        });
      }
      await ctx.db.delete(existing._id);
    }
    return null;
  },
});

